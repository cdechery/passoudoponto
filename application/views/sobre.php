	<h2>O que é?</h2>
	<p><ul>O Interessa.org é um site para facilitar a doação, especialmente entre pessoas, daquelas coisas que não queremos mais, estão entulhando nossas casas mas pode interessar a alguém. As doações podem ser para e entre instituições também, tais como ONGs, orfanatos, asilos, etc. Mas esse não é um site com foco na caridade, e sim numa alternativa ao desperdício e combate a <a href="http://pt.wikipedia.org/wiki/Obsolesc%C3%AAncia">obsolescência planejada</a> - com o foco na redução do consumo e reuso.</ul>

	<h3>Como funciona?</h3>

	<ul><h4>para pessoas:</h4>
	<p>Se você estiver procurando alguém interessado em algo que você não precisa ou não usa mais, basta você se cadastrar, depois incluir os itens que tem aí sobrando e acumulando pó. No mapa você consegue filtrar as pessoas interessadas nas categorias de item que você cadastrou. Se elas tiverem <i>Interesses</i> cadastrados, serão avisadas automaticamente das coisas que você cadastrar.</p>
	</ul>

	<ul>
	<h4>para instituições:</h4>
	<p>Usuários cadastrados como <i>Instituições</i> podem cadastrar Itens e Interesses da mesma forma que Pessoas. A diferença é que a validade dos Interesses é maior, e, como o foco da Instiuição é mais em receber itens do que doar, todas são exibidas no mapa independente de ter algo a doar ou não - dessa forma fica mais fácil achar alguma Interessada em receber aquilo que você quer doar.</p>
	</ul>

	<h3>Contato para doações</h3>
	<ul>
	<p>O site atua apenas como um grande cadastro de pessoas, instituições e itens disponíveis para doação. Todo contato e negociação para realização das coletas e ou entrega dos itens, é feito diretamente entre as pessoas - e o Interessa.org facilita (e protege) esse primeiro contato. Ao identificar um item que lhe interessa, por exemplo, você envia uma mensagem ao proprietário, se ele decidir responder, a partir desse momento toda a comunicação é feita diretamente por email.</p>
	</ul>

	<h3>Trocas e vendas</h3>
	<ul>
	<p>Trocas e vendas são proibidas aqui. O foco aqui é o desapego. Por favor, colabore.</p>
	</ul>

	<h3>Sustentabilidade</h3>
	<ul>
	<p>Além do nosso site estimular o segundo 'R' que forma os alicerces da sustentabilidade (Reduzir, Reutilizar, Reciclar), tentamos ir além, sempre que possível. Esse site está hospedado com uma empresa que tem 300% da energia que utiliza em fontes renováveis. Através de turbinas eólicas, além de serem totalmente independentes da rede elétrica, ainda sobra, e eles vendem de volta - garantindo emissão negativa de carbono.</p>
	</ul>
